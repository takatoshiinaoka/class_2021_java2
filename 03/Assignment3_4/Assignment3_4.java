class Person
{
    private String name; // 名前
    private int age; // 年齢（0 以上）
    private char gender; // 性別（F or M）
    public void setName(String n){// 名前を設定
        name=n;
    }
    public String getName(){// 名前を取得
        return name;
    } 
    public void setAge(int a){// 年齢を設定
        age=a;
    }
    public int getAge(){// 年齢を取得
        return age;
    }
    public void setGender(char g){// 性別を設定
        gender=g;
    }
    public char getGender(){// 性別を取得
        return gender;
    }
}
